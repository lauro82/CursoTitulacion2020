<?php
/**
 * PHP String functions
 */

 // strlen() devuelve la longitud de un String

echo strlen("Hola mundo");
echo "<br>";

// str_word_count() cuenta el numero de palabras en un String

echo str_word_count("Hola mundo");
echo "<br>";

// strrev() invierte un String

echo strrev("Buenos días!");
echo "<br>";

// strpos() buscar una texto dentro de un String

echo strpos("Saludos a todos", "todos");
echo "<br>";

// str_replace() remplaza  un texto dentro de un String

echo str_replace("mundo", "Héctor", "Hola mundo");